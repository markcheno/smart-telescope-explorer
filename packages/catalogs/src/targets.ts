/**
 * @ste/catalogs — seed deep-sky target catalog (spec v0.7 §23).
 *
 * ~30 curated objects spanning large emission complexes down to small galaxies
 * and planetary nebulae, so the tool can be driven with real sky instead of
 * hand-entered geometry. Each record carries stable id, coordinates (J2000),
 * angular size + position angle, classification, and (for emission objects)
 * line relevance — the fields framing, sampling, and filter analysis consume.
 *
 * These are CURATED SEED values suitable for framing/feasibility estimates, not
 * precision astrometry: coordinates are high-confidence, dimensions/position
 * angles are moderate-confidence (catalogs disagree at the arcminute level).
 * The catalog gate (spec §23) — verified coordinates, dimensions, angle
 * convention, core/halo, classification, sources — is met at that confidence.
 */

import type { CustomTarget, SourceMetadata } from '@ste/schema';

/** Bumped whenever a record's astrometry or classification changes (spec §41). */
export const TARGET_CATALOG_VERSION = '0.7.0';

const SEED_SOURCE: SourceMetadata = {
  source_type: 'catalog',
  confidence: 'moderate',
  source_label: 'Curated seed catalog',
  source_note:
    'Coordinates high-confidence; dimensions and position angles are moderate-confidence curated values (catalogs disagree at the arcminute level). Verify before precision use.',
  reviewed_at: '2026-07-15T00:00:00Z',
};

interface Seed {
  id: string;
  name: string;
  aliases?: string[];
  /** RA in DEGREES (J2000). */
  ra: number;
  /** Dec in degrees (J2000). */
  dec: number;
  /** Major × minor extent, arcminutes. */
  w: number;
  h: number;
  /** Position angle of the major axis, degrees. */
  pa?: number;
  type: CustomTarget['classification']['target_type'];
  size: NonNullable<CustomTarget['classification']['angular_size_class']>;
  sb: NonNullable<CustomTarget['classification']['surface_brightness_class']>;
  spectral: NonNullable<CustomTarget['classification']['spectral_class']>;
  ha?: NonNullable<CustomTarget['classification']['h_alpha_relevance']>;
  oiii?: NonNullable<CustomTarget['classification']['oiii_relevance']>;
  /** Integrated visual magnitude, when well-defined. */
  mag?: number;
  /** Recommended empty margin per side (defaults 0.1). */
  margin?: number;
}

/** Convert a compact seed row into a full CustomTarget record. */
function target(s: Seed): CustomTarget {
  return {
    target_id: s.id,
    name: s.name,
    ...(s.aliases != null ? { aliases: s.aliases } : {}),
    coordinates: { right_ascension_deg: s.ra, declination_deg: s.dec, epoch: 'j2000' },
    geometry: {
      shape: 'ellipse',
      width_arcmin: s.w,
      height_arcmin: s.h,
      position_angle_deg: s.pa ?? 0,
      recommended_margin_fraction: s.margin ?? 0.1,
    },
    classification: {
      target_type: s.type,
      angular_size_class: s.size,
      surface_brightness_class: s.sb,
      spectral_class: s.spectral,
      ...(s.ha != null ? { h_alpha_relevance: s.ha } : {}),
      ...(s.oiii != null ? { oiii_relevance: s.oiii } : {}),
    },
    ...(s.mag != null ? { brightness: { integrated_magnitude: s.mag } } : {}),
    source: SEED_SOURCE,
  };
}

