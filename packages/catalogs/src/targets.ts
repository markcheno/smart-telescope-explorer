/** The seed deep-sky catalog (~30 objects), spec v0.7 §23. */
export const TARGET_CATALOG: readonly CustomTarget[] = SEEDS.map(target);

/** Look up a catalog target by its stable id. */
export function findCatalogTarget(id: string): CustomTarget | undefined {
  return TARGET_CATALOG.find((t) => t.target_id === id);
}
